#include <iostream>
using namespace std;
int main() {
    cout << "Please enter the first number A ?" << endl;
    int A;  cin >> A;
    cout << "Please enter the second number B ?" << endl;
    int B;  cin >> B;
    cout << "A + B = " << A + B << endl;
    cout << "A - B = " << A - B << endl;
    cout << "A * B = " << A * B << endl;
    cout << "A / B = " << A / B << endl;
    cout << "A % B = " << A % B << endl;
}